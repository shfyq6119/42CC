#include "ft_printf.h"

int	ft_putstr_n(char *s)
{
	int	i;

	i = 0;
	if (!s)
		return (ft_putstr_n("(null)"));
	while (s[i])
		i += ft_putchar_n(s[i]);
	return (i);
}
