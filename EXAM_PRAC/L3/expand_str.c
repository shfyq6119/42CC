#include <unistd.h>

int	main(int ac, char **av)
{
	int	i = 0;
	int	flag = 0;
	if (ac == 2)
	{
		while (av[1][i])
		{
			while (av[1][i] && (av[1][i] == 32 || av[1][i] == 9))
				i++;
			if (flag && (av[1][i] && (av[1][i-1] == 32 || av[1][i-1] == 9)))
			{
				write(1, "   ", 3);
				flag = 0;
			}
			while (av[1][i] && (av[1][i] != 32 && av[1][i] != 9))
			{
				write(1, &av[1][i++], 1);
				flag = 1;
			}
		}
	}
	write(1, "\n", 1);
	return (0);
}
