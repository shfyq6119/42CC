#include <unistd.h>
int	main(int ac, char **av)
{
	int	i = 0;
	if (ac ==2)
	{
		while (av[1][i])
		{
			if (av[1][i] > 64 && av[1][i] < 91)
				av[1][i] = (av[1][i] - 65 + 1) % 26 + 65;
			else if (av[1][i] > 96 && av[1][i] < 123)
				av[1][i] = (av[1][i] - 97 + 1) % 26 + 97;
			write(1, &av[1][i], 1);
			i++;
		}
	}
	write(1, "\n", 1);
	return (0);
}

