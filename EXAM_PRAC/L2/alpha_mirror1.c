#include <unistd.h>

int	main(int arc, char **arv)
{
	int	i;

	i = 0;
	if (arc == 2)
	{
		while (arv[1][i])
		{
			if (arv[1][i] > 64 && arv[1][i] < 91)
				arv[1][i] = 77 - (arv[1][i] - 78);
			if (arv[1][i] > 96 && arv[1][i] < 123)
				arv[1][i] = 109 - (arv[1][i] - 110);
			write (1, &arv[1][i], 1);
			i++;
		}
	}
	write(1, "\n", 1);
	return (0);
}
