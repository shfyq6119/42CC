int	ft_atoi(char *str)
{
	int p = 1;
	int r = 0;
	while (*str == 32 || (*str >= 9 && str <= 13))
		str++;
	if (*str == 45)
		p -= p;
	if (*str == 45 || *str == 43)
		str++;
	while (*str >= 48 && *str <= 57)
	{
		r = r * 10 + *str - 48;
		str++;
	}
	return (p * r);
}
